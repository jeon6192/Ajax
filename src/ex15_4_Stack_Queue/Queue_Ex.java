package ex15_4_Stack_Queue;

import java.util.LinkedList;
import java.util.Queue;

/*
 *		큐(Queue) 인터페이스
 *		- LinkedList (Queue 인터페이스를 구현한 클래스)를 사용
 *		- FIFO(First Input First Output) 구조
 *		  먼저 입력된 자료가 먼저 출력되는 구조 
 */

public class Queue_Ex {
	public static void main(String[] args) {
		// 큐로 사용할 LinkedList객체를 생성합니다.
		// LinkedList<String> queue=new LinkedList<String>();
		Queue<String> queue=new LinkedList<String>();
		System.out.println("비었나요? "+queue.isEmpty());	// boolean형 값 반환
		
		// offer() 메소드 : 큐에 데이터를 추가
		queue.offer("토끼");
		queue.offer("사슴");
		queue.offer("호랑이");
		
		// peek() 메소드 : 처음 데이터를 그대로 둔상태로 가져옵니다.
		System.out.println(queue.peek());	// 데이터를 꺼내지 않고 확인만 함
		System.out.println("비었나요? "+queue.isEmpty());
		System.out.println("갯수 : "+queue.size());
		
		System.out.println("poll() 사용");
		while(!queue.isEmpty()) {
			// poll() 메소드는 큐에 있는 데이터를 제거하면서 가져옵니다.
			System.out.println("데이터 출력 : "+queue.poll());
		}
		System.out.println("비었나요? "+queue.isEmpty());
		
	}
}
